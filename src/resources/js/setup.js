const maxModelSize = 513;
const maxCameraSize = 513;
const videoWidth = 600;
const videoHeight = 500;

function isAndroid() {
  return /Android/i.test(navigator.userAgent);
}

function isiOS() {
  return /iPhone|iPad|iPod/i.test(navigator.userAgent);
}

function isMobile() {
  return isAndroid() || isiOS();
}

async function setupVideo(){
    var model_video = document.getElementById('model-video');
    model_video.width = maxModelSize;
    model_video.height = maxModelSize;
    return model_video;
}

async function load_modelVideo() {
    const model_video = await setupVideo();
    return model_video;
}

function playVid() { 
  model_video.play(); 
} 

function pauseVid() { 
  model_video.pause(); 
} 

/**
 * Loads a the camera to be used in the demo
 *
 */
async function setupCamera() {
  if (!navigator.mediaDevices || !navigator.mediaDevices.getUserMedia) {
    throw new Error(
        'Browser API navigator.mediaDevices.getUserMedia not available');
  }

  const video = document.getElementById('camera-video');
  video.width = videoWidth;
  video.height = videoHeight;

  const mobile = isMobile();
  const stream = await navigator.mediaDevices.getUserMedia({
    'audio': false,
    'video': {
      facingMode: 'user',
      width: mobile ? undefined : videoWidth,
      height: mobile ? undefined : videoHeight,
    },
  });
  video.srcObject = stream;

  return new Promise((resolve) => {
    video.onloadedmetadata = () => {
      resolve(video);
    };
  });
}

async function load_cameraVideo() {
  const camera_video = await setupCamera();
  camera_video.play();

  return camera_video;
}

navigator.getUserMedia = navigator.getUserMedia || navigator.webkitGetUserMedia || navigator.mozGetUserMedia;
